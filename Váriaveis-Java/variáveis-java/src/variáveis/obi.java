package variáveis;

public class obi {
	public static void main(String[] args) {
		
		double N;
		N = 31;
		
		if(N <= 10) {
			System.out.println(7);
		} else if (N <= 30) {
			System.out.println(7 + (N - 10));
		} else if (N <= 100) {
			System.out.println(7 + (N - 30));
	}
  }
}
